class AssessmentLink(models.Model):
    """Links generated by business users for candidates"""
    job_role = models.ForeignKey(JobRole, on_delete=models.CASCADE, related_name='assessment_links')
    unique_link = models.UUIDField(default=uuid.uuid4, unique=True, editable=False)
    access_code = models.CharField(max_length=8, unique=True)
    is_active = models.BooleanField(default=True)
    expires_at = models.DateTimeField(null=True, blank=True)
    created_at = models.DateTimeField(auto_now_add=True)
    
    def save(self, *args, **kwargs):
        if not self.access_code:
            self.access_code = self.generate_access_code()
        super().save(*args, **kwargs)
    
    def generate_access_code(self):
        """Generate a unique 8-character access code"""
        while True:
            code = ''.join(random.choices(string.ascii_uppercase + string.digits, k=8))
            if not AssessmentLink.objects.filter(access_code=code).exists():
                return code
    
    def __str__(self):
        return f"{self.job_role.title} - {self.access_code}"

