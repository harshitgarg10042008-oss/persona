class IndividualAssessment(models.Model):
    """Individual practice assessments using platform job titles"""
    STATUS_CHOICES = [
        ('pending', 'Pending'),
        ('in_progress', 'In Progress'),
        ('completed', 'Completed'),
        ('terminated', 'Terminated'),
    ]
    
    user = models.ForeignKey(User, on_delete=models.CASCADE, related_name='individual_assessments')
    platform_job_title = models.ForeignKey(PlatformJobTitle, on_delete=models.CASCADE)
    status = models.CharField(max_length=20, choices=STATUS_CHOICES, default='pending')
    session_id = models.UUIDField(default=uuid.uuid4, unique=True)
    
    # Assessment configuration
    total_questions = models.PositiveIntegerField(default=0)
    current_question_index = models.PositiveIntegerField(default=0)
    selected_questions = models.JSONField(default=list, help_text="List of question IDs for this assessment")
    
    # Timing
    started_at = models.DateTimeField(null=True, blank=True)
    completed_at = models.DateTimeField(null=True, blank=True)
    estimated_duration = models.PositiveIntegerField(default=1800, help_text="Estimated duration in seconds")
    
    # Security and monitoring
    fullscreen_violations = models.PositiveIntegerField(default=0)
    warnings_given = models.PositiveIntegerField(default=0)
    browser_focus_lost = models.PositiveIntegerField(default=0)
    
    # Analysis scores (filled after completion)
    overall_score = models.FloatField(null=True, blank=True)
    body_language_score = models.FloatField(null=True, blank=True)
    attire_score = models.FloatField(null=True, blank=True)
    speaking_score = models.FloatField(null=True, blank=True)
    relevance_score = models.FloatField(null=True, blank=True)
    
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)
    
    def __str__(self):
        return f"{self.user.email} - {self.platform_job_title.title} ({self.status})"
    
    def get_next_question(self):
        """Get the next question for this assessment"""
        if self.current_question_index < len(self.selected_questions):
            question_id = self.selected_questions[self.current_question_index]
            return PlatformQuestion.objects.get(id=question_id)
        return None
    
    def select_questions(self):
        """Select questions for this assessment (mandatory + random non-mandatory)"""
        if self.selected_questions:
            return  # Already selected
            
        mandatory_questions = list(
            self.platform_job_title.questions.filter(is_mandatory=True, is_active=True)
            .values_list('id', flat=True)
        )
        
        non_mandatory_questions = list(
            self.platform_job_title.questions.filter(is_mandatory=False, is_active=True)
            .values_list('id', flat=True)
        )
        
        # Select up to 5 random non-mandatory questions
        random_questions = random.sample(
            non_mandatory_questions, 
            min(5, len(non_mandatory_questions))
        )
        
        all_selected = mandatory_questions + random_questions
        random.shuffle(all_selected)  # Randomize order
        
        self.selected_questions = all_selected
        self.total_questions = len(all_selected)
        self.save()


